result = [hasattr(obj, '__iter__') for obj in objects]
# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç: True ‚Äî –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–π, False ‚Äî –Ω–µ –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–π
