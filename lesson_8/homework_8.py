# # 4. –°–æ–∑–¥–∞–π—Ç–µ —Å–ø–∏—Å–æ–∫ –∏–∑ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 20, –≥–¥–µ –≤–º–µ—Å—Ç–æ —á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –±—É–¥–µ—Ç "—á–µ—Ç–Ω–æ–µ",
# # –∞ –≤–º–µ—Å—Ç–æ –Ω–µ—á–µ—Ç–Ω—ã—Ö ‚Äî "–Ω–µ—á–µ—Ç–Ω–æ–µ" (–∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Ç–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä)
# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ —Å –ø–æ–º–æ—â—å—é —Å–ø–∏—Å–∫–æ–≤–æ–≥–æ –≤–∫–ª—é—á–µ–Ω–∏—è (list comprehension)
# –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ç–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä: –∑–Ω–∞—á–µ–Ω–∏–µ_–µ—Å–ª–∏_–∏—Å—Ç–∏–Ω–∞ if —É—Å–ª–æ–≤–∏–µ else –∑–Ω–∞—á–µ–Ω–∏–µ_–µ—Å–ª–∏_–ª–æ–∂—å
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# range(1, 21) –¥–∞—ë—Ç —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 20 –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# –¢–µ—Ä–Ω–∞—Ä–Ω—ã–π –æ–ø–µ—Ä–∞—Ç–æ—Ä –≤–Ω—É—Ç—Ä–∏ list comprehension:
# "—á–µ—Ç–Ω–æ–µ" if num % 2 == 0 else "–Ω–µ—á–µ—Ç–Ω–æ–µ"
# –≠—Ç–æ –ø–æ–∑–≤–æ–ª—è–µ—Ç –∑–∞–º–µ–Ω–∏—Ç—å –∫–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â—É—é —Å—Ç—Ä–æ–∫—É –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É –∫–æ–¥–∞.
