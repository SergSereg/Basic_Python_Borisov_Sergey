#
# 123 (—á–∏—Å–ª–æ) ‚Äî –Ω–µ –∏—Ç–µ—Ä–∏—Ä—É–µ–º–æ–µ ‚Üí False
# "—Ç–µ–∫—Å—Ç" (—Å—Ç—Ä–æ–∫–∞) ‚Äî –∏—Ç–µ—Ä–∏—Ä—É–µ–º–æ–µ ‚Üí True
# [1, 2, 3] (—Å–ø–∏—Å–æ–∫) ‚Äî –∏—Ç–µ—Ä–∏—Ä—É–µ–º–æ–µ ‚Üí True

