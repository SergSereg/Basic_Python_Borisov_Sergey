# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ —á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç -10 –¥–æ 10 —Å –ø–æ–º–æ—â—å—é —Å–ø–∏—Å–∫–æ–≤–æ–≥–æ –≤–∫–ª—é—á–µ–Ω–∏—è (list comprehension), 11 –Ω–µ –≤–∫–ª—é—á–∞–µ—Ç—Å—è
# –ò—Å–ø–æ–ª—å–∑—É–µ–º range(-10, 11), –ø–æ—Ç–æ–º—É —á—Ç–æ –ø—Ä–∞–≤–∞—è –≥—Ä–∞–Ω–∏—Ü–∞ –≤ range –Ω–µ –≤–∫–ª—é—á–∞–µ—Ç—Å—è
# –£—Å–ª–æ–≤–∏–µ "if num % 2 == 0" –æ—Ç–±–∏—Ä–∞–µ—Ç —Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞
even_numbers = [num for num in range(-10, 11) if num % 2 == 0]
# –í—ã–≤–æ–¥–∏–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫
print(even_numbers)


# # ===============================================
# # 3. –î–∞–Ω —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤. –°–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –¥–ª–∏–Ω—ã –≤—Å–µ—Ö —Å–ª–æ–≤, –∏—Å–ø–æ–ª—å–∑—É—è –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä —Å–ø–∏—Å–∫–æ–≤.
# # ===============================================

# –ó–∞–¥–∞—ë–º –∏—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤ (–º–æ–∂–Ω–æ –∑–∞–º–µ–Ω–∏—Ç—å –Ω–∞ –ª—é–±–æ–π –¥—Ä—É–≥–æ–π)
words = ["–ü—Ä–∏–≤–µ—Ç", "–º–∏—Ä", "Python", "—Ü–∏–∫–ª", "–≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä"]
# –°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ —Å –ø–æ–º–æ—â—å—é —Å–ø–∏—Å–∫–æ–≤–æ–≥–æ –≤–∫–ª—é—á–µ–Ω–∏—è (list comprehension)
# –î–ª—è –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞ –≤ —Å–ø–∏—Å–∫–µ words –≤—ã—á–∏—Å–ª—è–µ–º –µ–≥–æ –¥–ª–∏–Ω—É —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ len()
# len(word) –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ.
# –°–ø–∏—Å–∫–æ–≤–æ–µ –≤–∫–ª—é—á–µ–Ω–∏–µ [len(word) for word in words] ‚Äî –∫—Ä–∞—Ç–∫–∏–π –∏ —á–∏—Ç–∞–µ–º—ã–π —Å–ø–æ—Å–æ–± –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞.
lengths = [len(word) for word in words]
# –í—ã–≤–æ–¥–∏–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –¥–ª–∏–Ω —Å–ª–æ–≤
print(lengths)

# üîÑ –ö–∞–∫ —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–∞ –ø—Ä–∏–º–µ—Ä–µ:
# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫:
# ["–ü—Ä–∏–≤–µ—Ç", "–º–∏—Ä", "Python", "—Ü–∏–∫–ª", "–≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä"]
#
# –î–ª–∏–Ω—ã —Å–ª–æ–≤:
