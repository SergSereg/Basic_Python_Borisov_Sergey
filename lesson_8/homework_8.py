# # 1. –°–æ–∑–¥–∞–π—Ç–µ —Å–ø–∏—Å–æ–∫ –∏–∑ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ N, –∏—Å–ø–æ–ª—å–∑—É—è list comprehension.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
N = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ N: "))
# –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ N —Å –ø–æ–º–æ—â—å—é —Å–ø–∏—Å–∫–æ–≤–æ–≥–æ –≤–∫–ª—é—á–µ–Ω–∏—è (list comprehension)
# –°–∏–Ω—Ç–∞–∫—Å–∏—Å: [–≤—ã—Ä–∞–∂–µ–Ω–∏–µ for –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è in –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å]
# range(1, N + 1) –¥–∞—ë—Ç —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ N –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# num ** 2 ‚Äî –≤–æ–∑–≤–µ–¥–µ–Ω–∏–µ –≤ –∫–≤–∞–¥—Ä–∞—Ç.
