# # –°–æ–∑–¥–∞–π—Ç–µ –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä –≤ –∫–æ—Ç–æ—Ä–æ–º –±—É–¥–µ—Ç –Ω–∞–ø–∏—Å–∞–Ω–æ True - –µ—Å–ª–∏ –æ–±—ä–µ–∫—Ç —è–≤–ª—è–µ—Ç—Å—è –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–º –∏–ª–∏ False - –µ—Å–ª–∏ –Ω–µ—Ç.
# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ –∏–∑ —Ç—Ä—ë—Ö –æ–±—ä–µ–∫—Ç–æ–≤: —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ, —Å—Ç—Ä–æ–∫–∞ –∏ —Å–ø–∏—Å–æ–∫
objects = [123, "—Ç–µ–∫—Å—Ç", [1, 2, 3]]
# –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–∫–æ–≤–æ–µ –≤–∫–ª—é—á–µ–Ω–∏–µ (–ø–æ —Å–º—ã—Å–ª—É ‚Äî –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä —Å–ø–∏—Å–∫–∞),
