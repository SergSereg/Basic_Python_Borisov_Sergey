# "–º–∏—Ä" ‚Üí 3 –±—É–∫–≤—ã
# "Python" ‚Üí 6 –±—É–∫–≤
# "—Ü–∏–∫–ª" ‚Üí 4 –±—É–∫–≤—ã
# "–≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä" ‚Üí 9 –±—É–∫–≤
