# –í—ã–≤–æ–¥–∏–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫
print(squares)
# üîÑ –ö–∞–∫ —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–∞ –ø—Ä–∏–º–µ—Ä–µ: N = 5
# range(1, 6) ‚Üí 1, 2, 3, 4, 5
# –ö–≤–∞–¥—Ä–∞—Ç—ã:
# 1¬≤ = 1
# 2¬≤ = 4
# 3¬≤ = 9
# 4¬≤ = 16
# 5¬≤ = 25
# –†–µ–∑—É–ª—å—Ç–∞—Ç: [1, 4, 9, 16, 25]



# # ===============================================
