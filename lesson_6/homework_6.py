# #++++++++++++++++++++++++++++++++++++++++++++++++
# # Условный оператор
# #++++++++++++++++++++++++++++++++++++++++++++++++
# #================================================
# # 1. Проверка знака числа
# #================================================
x = int(input("Введите число: "))  # Преобразуем строку в целое число (int)

if x > 0: # Проверяем: если x больше нуля → число положительное
    print("Число положительное")  # Выводим сообщение для положительного числа

elif x < 0: # Если первое условие не сработало, проверяем: меньше ли x нуля?
    print("Число отрицательное")  # Выводим сообщение для отрицательного числа

else: # Если ни одно из условий выше не выполнилось (остался только x == 0)
    print("Число равно нулю")  # Выводим сообщение для нуля

# #================================================
# # 2. Проверка на четность
# #================================================
x = int(input("Введите целое число: ")) # input() возвращает строку

if x % 2 == 0: # Проверяем, делится ли число x на 2 без остатка (оператор % возвращает остаток от деления)
    print("Число четное")

else: # Если первое условие не выполнилось (остаток не 0) → число нечётное
    print("Число нечетное")

# #================================================
# # 3. Определение диапазона
# #================================================
x = int(input("Введите число: ")) # input() возвращает строку

if x >= 1 and x <= 10:  # Если оба условия верны → число в диапазоне от 1 до 10 включительно.
    print("Число в диапазоне")

else: # Если хотя бы одно условие неверно → число вне диапазона [1; 10]
    print("Число вне диапазоне")

# #================================================
# # 4. Перестановка двух чисел
# #================================================
a = int(input("Введите первое число: "))

b = int(input("Введите второе число: "))

if a < b: # Проверяем: если a меньше b, то нужно поменять их местами
    a, b = b, a  # Справа создаётся временный кортеж (b, a), затем распаковывается в (a, b)

print(a, b) # Выводим числа в порядке убывания (a должно быть >= b после перестановки)

# #================================================
# # 5. Поиск минимума из двух чисел
# #================================================
a = int(input("Введите первое число: "))

b = int(input("Введите второе число: "))

if a <= b: # Сравниваем два числа: если a меньше или равно b
    print(a) # Тогда a — меньшее (или равное) число, выводим его

else: # Если a больше b
    print(b) # Тогда b — меньшее число, выводим его

# #================================================
# # 6. Проверка наличия элемента в списке
# #================================================
marks = [3, 4, 5, 2, 5, 4] # Создаём список оценок — список целых чисел

if 2 in marks: # Проверяем, содержится ли число 2 в списке marks
               # Оператор `in` возвращает True, если элемент найден, иначе False
    print("Есть неудовлетворительная оценка : ") # Если двойка есть в списке выводим сообщение о неудовлетворительной оценке

else: # Если условие выше не выполнилось (двойки нет в списке)
    print("Все оценки положительные: ")  # Выводим сообщение, что все оценки положительные

# #================================================
# # 7. Проверка делимости
# #================================================
x = int(input("Введите число: "))

if x % 3 == 0 and x % 5 == 0: # Сначала проверяем, делится ли x на 3 И на 5 одновременно
    print("Число делится на 3 и на 5") # Если оба условия верны → число кратно и 3, и 5

elif x % 3 == 0: # Если первое условие не выполнилось, проверяем: делится ли только на 3?
    print("Число делится только на 3") # Это значит: делится на 3, но НЕ делится на 5 (иначе сработало бы первое условие)

elif x % 5 == 0: # Если и второе условие не выполнилось, проверяем: делится ли только на 5?
    print("Число делится только на 5") # Это значит: делится на 5, но НЕ делится на 3

else: # Если ни одно из условий выше не сработало
    print("Число не делится на 3 и 5") # Значит, число не делится ни на 3, ни на 5

# #================================================
# # 8. Оператор in для проверки пароля
# #================================================
password = input("Введите пароль: ") # input() возвращает строку, и пароль — это строка

if password == "admin123": # Проверяем, совпадает ли введённый пароль со строкой "admin123"
                           # Используем оператор == для точного сравнения строк
    print("Доступ разрешен")   # Если пароль верный → разрешаем доступ

else: # Если пароль не совпадает
    print("Доступ запрещен")   # Запрещаем доступ

# #================================================
# # 9. Калькулятор скидки
# #================================================
amount = int(input("Введите сумму покупки: "))# Запрашиваем у пользователя сумму покупки и преобразуем её в целое число

if amount >= 5000: # Проверяем: если сумма больше или равна 5000 → даём скидку 10%
    final_amount = amount * 0.9 # Рассчитываем итоговую сумму: вычитаем 10% (умножаем на 0.9)

elif amount >= 1000: # Если первое условие не выполнилось, проверяем: сумма >= 1000?
    final_amount = amount * 0.95 # Даём скидку 5% → умножаем на 0.95

else: # Если ни одно из условий выше не сработало (сумма < 1000)
    final_amount = amount # Скидка не положена → итоговая сумма равна исходной

print(final_amount) # Выводим итоговую сумму после применения скидки

# #================================================
# # 10. Определение високосного года
# #================================================
year = int(input("Введите год: "))
## Скобки () задают правильный порядок операций сначала проверяется (делится на 4 И не на 100), затем — ИЛИ (делится на 400).
if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0): # Год високосный, если:
                                                             # (делится на 4) И (НЕ делится на 100) ИЛИ (делится на 400)
    print("Год високосный") # Если условие выполняется → год високосный

else: # Если условие не выполняется
    print("Год не високосный") # Год не високосный

# #++++++++++++++++++++++++++++++++++++++++++++++++
# # Вложенные условия и elif
# #++++++++++++++++++++++++++++++++++++++++++++++++
# #================================================
# # 1. Проверка оценки
# #================================================
grade = int(input("Ведите оценку (от 1 до 5): ")) # Запрашиваем у пользователя оценку и преобразуем её в целое число

if grade < 1 or grade > 5: # Сначала проверяем, находится ли оценка в допустимом диапазоне [1; 5]
    print("Некорректная оценка") # Если оценка меньше 1 или больше 5 → выводим сообщение об ошибке

elif grade == 5: # Если оценка в диапазоне, проверяем конкретные значения через elif
    print("Отлично!") # Оценка 5 → "Отлично!"

elif grade == 4:
    print("Хорошо") # Оценка 4 → "Хорошо"

elif grade == 3:
    print("Удовлетворительно") # Оценка 3 → "Удовлетворительно"

else: # Остаются только оценки 1 и 2 (так как диапазон уже проверен)
    print("Неудовлетворительно")  # Выводим "Неудовлетворительно" для 1 и 2

# # ================================================
# # 2. Определение времени суток по часам
# # ================================================
hour = int(input("Введите текущее время в часах (0-23): ")) # Запрашиваем у пользователя текущее время

if hour < 0 or hour > 23: # Сначала проверяем, находится ли час в допустимом диапазоне [0; 23]
    print("Некорректное время") # Если час меньше 0 или больше 23 → выводим сообщение об ошибке

elif 6 <= hour < 11: # Проверяем: утро — с 6 до 11 включительно
    print("Утро")  # Выводим сообщение для утра

elif 12 <= hour <= 17: # Проверяем: день — с 12 до 17 включительно
    print("День") # Выводим сообщение для дня

elif 18 <= hour <= 21: # Проверяем: вечер — с 18 до 21 включительно
    print("Вечер") # Выводим сообщение для вечера

else: # Остаётся только ночь — с 22 до 23 и с 0 до 5
    print("Ночь") # Выводим сообщение для ночи

# # ================================================
# # 3. Классификация температуры
# # ================================================
temp = int(input("Введите температуру: ")) # Запрашиваем у пользователя температуру и преобразуем её в целое число

if temp < -10: # Проверяем: если температура ниже -10 → "Очень холодно"
    print("Очень холодно")

elif -10 <= temp <= 0: # Проверяем: если температура от -10 до 0 включительно → "Холодно"
    print("Холодно")

elif 1 <= temp <= 10: # Проверяем: если температура от 1 до 10 включительно → "Прохладно"
    print("Прохладно")

elif 11 <= temp <= 25: # Проверяем: если температура от 11 до 25 включительно → "Тепло"
    print("Тепло")

else: # Остаётся только случай: температура выше 25 → "Жарко"
    print("Жарко")

# # ================================================
# # 4. Проверка года на високосность
# # ================================================
year = int(input("Введите год: "))

if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0): # делится на 4 И не делится на 100) ИЛИ (делится на 400)
    print("Год високосный")  # Если условие выполняется → год високосный

else: # Если условие не выполняется
    print("Год не високосный")

# # ================================================
# # 5. Калькулятор двух чисел
# # ================================================
a = int(input("Введите первое число: "))

b = int(input("Введите второе число: "))

operation = input("Введите операцию (+, -, *, /,): ") # Запрашиваем у пользователя операцию (+, -, *, /)

if operation == "+": # Проверяем, какая операция введена
    print(a + b)  # Если операция '+' → выводим сумму чисел

elif operation == "-":
    print(a - b)    # Если операция '-' → выводим разность (a - b)

elif operation == "*":
    print(a * b)   # Если операция '*' → выводим произведение

elif operation == "/":
    if b == 0:  # Если операция '/' → сначала проверяем деление на ноль
        print("Ошибка: деление на ноль!") # Если делитель (b) равен нулю → выводим сообщение об ошибке
    else:
        print(a / b) # Если делитель не ноль → выполняем деление и выводим результат

else:
    print("Некорректная операция")  # Если введена любая другая операция (например, "%", "^", "abc")

# #++++++++++++++++++++++++++++++++++++++++++++++++
# # Тернарный оператор
# #++++++++++++++++++++++++++++++++++++++++++++++++
# #================================================
# # 1. Проверка четности числа
# #================================================
x = int(input("Введите число: "))
## Синтаксис: значение_если_истина if условие else значение_если_ложь (Используем тернарный оператор)
message = "Число четное" if x % 2 == 0 else "Число нечетное"

print(message) # Выводим результат

# #================================================
# # 2. Определение наибольшего числа
# #================================================
a = int(input("Введите первое число: "))

b = int(input("Введите второе число: "))
##  Используем тернарный оператор для выбора наибольшего числа:
max_number = a if a >= b else b # Если a >= b, то результат — a, иначе — b

print(max_number) # Выводим наибольшее число

# #================================================
# # 3. Проверка положительного или отрицательного числа
# #================================================
x = int(input("Введите число: "))
## Используем вложенный тернарный оператор:
## Сначала проверяем, больше ли x нуля → "Положительное"
## Если нет, то проверяем, меньше ли x нуля → "Отрицательное"
## Иначе → "Ноль"
message = "Положительное" if x > 0 else ("Отрицательное" if x < 0 else "Ноль")

print(message) # Выводим результат

# #================================================
# # 4. Определение возраста для входа в клуб
# #================================================
age = int(input("Введите ваш возраст: "))
## Используем тернарный оператор:
## Если возраст >= 18 → "Вход разрешен"
## Иначе → "Вход запрещен"
message = "Вход разрешен" if age >= 18 else "Вход запрещен"

print(message) # Выводим результат

# #================================================
# # 5. Определение скидки в магазине
# #================================================
amount = int(input("Введите сумму покупки: ")) # Запрашиваем у пользователя сумму покупки
## Используем тернарный оператор для расчёта итоговой суммы:
## Если сумма > 5000 → применяем скидку 10% (умножаем на 0.9)
## Иначе → оставляем сумму без изменений
final_amount = amount * 0.9 if amount > 5000 else amount

print(final_amount) # Выводим итоговую сумму после применения скидки






