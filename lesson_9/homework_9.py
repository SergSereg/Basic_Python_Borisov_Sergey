# # 1.–°–æ–∑–¥–∞–π—Ç–µ —Å–ª–æ–≤–∞—Ä—å, –≤ –∫–æ—Ç–æ—Ä–æ–º —Ö—Ä–∞–Ω—è—Ç—Å—è –Ω–∞–∑–≤–∞–Ω–∏—è —Ñ—Ä—É–∫—Ç–æ–≤ –∏ –∏—Ö —Ü–µ–Ω—ã –∑–∞ 1 –∫–≥.
# # –î–æ–±–∞–≤—å—Ç–µ –≤ —Å–ª–æ–≤–∞—Ä—å –µ—â–µ –æ–¥–∏–Ω —Ñ—Ä—É–∫—Ç –∏ –≤—ã–≤–µ–¥–∏—Ç–µ –∏—Ç–æ–≥–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å.
# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ª–æ–≤–∞—Ä—å —Å –Ω–∞–∑–≤–∞–Ω–∏—è–º–∏ —Ñ—Ä—É–∫—Ç–æ–≤ (–∫–ª—é—á–∏) –∏ –∏—Ö —Ü–µ–Ω–∞–º–∏ –∑–∞ 1 –∫–≥ (–∑–Ω–∞—á–µ–Ω–∏—è)
fruits = {
    '—è–±–ª–æ–∫–æ': 90,
    '–±–∞–Ω–∞–Ω': 60,
    '–∞–ø–µ–ª—å—Å–∏–Ω': 120
}
# –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—ã–π —Ñ—Ä—É–∫—Ç –≤ —Å–ª–æ–≤–∞—Ä—å, –∏—Å–ø–æ–ª—å–∑—É—è –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–µ —Å–∫–æ–±–∫–∏ –∏ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ
fruits['–º–∞–Ω–≥–æ'] = 250
# –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å
print(fruits)
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# –ö–ª—é—á–∏ ‚Äî —Å—Ç—Ä–æ–∫–∏ ("—è–±–ª–æ–∫–æ" –∏ —Ç.–¥.), –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî —á–∏—Å–ª–∞ (—Ü–µ–Ω—ã).
# –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –Ω–æ–≤–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –¥–µ–ª–∞–µ—Ç—Å—è –ø—Ä–æ—Å—Ç–æ: —Å–ª–æ–≤–∞—Ä—å[–∫–ª—é—á] = –∑–Ω–∞—á–µ–Ω–∏–µ.



# # ===============================================
# # 2. –î–∞–Ω —Å–ª–æ–≤–∞—Ä—å —Å –æ—Ü–µ–Ω–∫–∞–º–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤:
# # grades = {"–ê–Ω–Ω–∞": 5, "–ë–æ—Ä–∏—Å": 4, "–í–∏–∫—Ç–æ—Ä": 3, "–ì–∞–ª–∏–Ω–∞": 5, "–î–º–∏—Ç—Ä–∏–π": 2}
# # –í—ã–≤–µ–¥–∏—Ç–µ –∏–º–µ–Ω–∞ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, —É –∫–æ—Ç–æ—Ä—ã—Ö –æ—Ü–µ–Ω–∫–∞ –Ω–µ –Ω–∏–∂–µ 4.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å: –∫–ª—é—á–∏ ‚Äî –∏–º–µ–Ω–∞ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî –∏—Ö –æ—Ü–µ–Ω–∫–∏
grades = {'–ê–Ω–Ω–∞': 5, '–ë–æ—Ä–∏—Å': 4, '–í–∏–∫—Ç–æ—Ä': 3, '–ì–∞–ª–∏–Ω–∞': 5, '–î–º–∏—Ç—Ä–∏–π': 2}
# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –≤—Å–µ –ø–∞—Ä—ã (–∫–ª—é—á, –∑–Ω–∞—á–µ–Ω–∏–µ) –≤ —Å–ª–æ–≤–∞—Ä–µ —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ items()
for name, score in grades.items():
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –æ—Ü–µ–Ω–∫–∞ –Ω–µ –Ω–∏–∂–µ 4 (—Ç.–µ. >= 4)
    if score >=4:
        # –ï—Å–ª–∏ —É—Å–ª–æ–≤–∏–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è ‚Äî –≤—ã–≤–æ–¥–∏–º –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞
        print(name)
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# grades.items() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–∞—Ä—ã (–∫–ª—é—á, –∑–Ω–∞—á–µ–Ω–∏–µ).
# –ú—ã —Ä–∞—Å–ø–∞–∫–æ–≤—ã–≤–∞–µ–º –∏—Ö –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ name –∏ score.
# –£—Å–ª–æ–≤–∏–µ score >= 4 –æ—Ç–±–∏—Ä–∞–µ—Ç –æ—Ü–µ–Ω–∫–∏ 4 –∏ 5.



# # ===============================================
# # 3. –°–æ–∑–¥–∞–π—Ç–µ —Å–ª–æ–≤–∞—Ä—å, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –Ω–∞–∑–≤–∞–Ω–∏–µ —Å—Ç—Ä–∞–Ω –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∫–ª—é—á–µ–π –∏ –∏—Ö —Å—Ç–æ–ª–∏—Ü—ã –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∑–Ω–∞—á–µ–Ω–∏–π.
# # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–æ–¥–∏—Ç –Ω–∞–∑–≤–∞–Ω–∏–µ —Å—Ç—Ä–∞–Ω—ã.
# # –í—ã–≤–µ–¥–∏—Ç–µ —Å—Ç–æ–ª–∏—Ü—É —ç—Ç–æ–π —Å—Ç—Ä–∞–Ω—ã (–µ—Å–ª–∏ —Å—Ç—Ä–∞–Ω–∞ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä–µ, –≤—ã–≤–µ–¥–∏—Ç–µ "–°—Ç—Ä–∞–Ω–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞").
# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ª–æ–≤–∞—Ä—å: –∫–ª—é—á–∏ ‚Äî –Ω–∞–∑–≤–∞–Ω–∏—è —Å—Ç—Ä–∞–Ω, –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî –∏—Ö —Å—Ç–æ–ª–∏—Ü—ã
countries = {
    '–†–æ—Å—Å–∏—è': '–ú–æ—Å–∫–≤–∞',
    '–§—Ä–∞–Ω—Ü–∏—è': '–ü–∞—Ä–∏–∂',
    '–ì–µ—Ä–º–∞–Ω–∏—è': '–ë–µ—Ä–ª–∏–Ω',
    '–ò—Ç–∞–ª–∏—è': '–†–∏–º',
    '–Ø–ø–æ–Ω–∏—è': '–¢–æ–∫–∏–æ'
}
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞–∑–≤–∞–Ω–∏–µ —Å—Ç—Ä–∞–Ω—ã
country = input('–í–≤–µ–¥–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ —Å—Ç—Ä–∞–Ω—ã: ')
# –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —Ç–∞–∫–∞—è —Å—Ç—Ä–∞–Ω–∞ –≤ —Å–ª–æ–≤–∞—Ä–µ
if country in countries:
    # –ï—Å–ª–∏ –µ—Å—Ç—å ‚Äî –≤—ã–≤–æ–¥–∏–º –µ—ë —Å—Ç–æ–ª–∏—Ü—É
    print(countries[country])
else:
    # –ï—Å–ª–∏ –Ω–µ—Ç ‚Äî –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
    print('–°—Ç—Ä–∞–Ω–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞')
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# country in countries –ø—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –∫–ª—é—á–∞ –≤ —Å–ª–æ–≤–∞—Ä–µ.
# –≠—Ç–æ –±–µ–∑–æ–ø–∞—Å–Ω–µ–µ, —á–µ–º —Å—Ä–∞–∑—É –ø–∏—Å–∞—Ç—å countries[country] ‚Äî –∏–∑–±–µ–≥–∞–µ–º KeyError.
# –†–µ–≥–∏—Å—Ç—Ä –≤–∞–∂–µ–Ω: "—Ä–æ—Å—Å–∏—è" ‚â† "–†–æ—Å—Å–∏—è".



# # ===============================================
# # 4. –î–∞–Ω —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –∏ –∏—Ö –∫—É—Ä—Å–æ–≤:
# # students = [
# #     ("–ê–Ω–Ω–∞", "Python"),
# #     ("–ë–æ—Ä–∏—Å", "Java"),
# #     ("–í–∏–∫—Ç–æ—Ä", "Python"),
# #     ("–ì–∞–ª–∏–Ω–∞", "C++"),
# #     ("–î–º–∏—Ç—Ä–∏–π", "Python")
# # ]
# # –°–æ–∑–¥–∞–π—Ç–µ —Å–ª–æ–≤–∞—Ä—å, –≤ –∫–æ—Ç–æ—Ä–æ–º –∫–ª—é—á–∞–º–∏ –±—É–¥—É—Ç –Ω–∞–∑–≤–∞–Ω–∏—è –∫—É—Ä—Å–æ–≤, –∞ –∑–Ω–∞—á–µ–Ω–∏—è–º–∏ ‚Äî —Å–ø–∏—Å–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –∏–∑—É—á–∞—é—Ç —ç—Ç–æ—Ç –∫—É—Ä—Å.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π: (–∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞, –Ω–∞–∑–≤–∞–Ω–∏–µ –∫—É—Ä—Å–∞)
students = [
    ("–ê–Ω–Ω–∞", "Python"),
    ("–ë–æ—Ä–∏—Å", "Java"),
    ("–í–∏–∫—Ç–æ—Ä", "Python"),
    ("–ì–∞–ª–∏–Ω–∞", "C++"),
    ("–î–º–∏—Ç—Ä–∏–π", "Python")
]
# –°–æ–∑–¥–∞—ë–º –ø—É—Å—Ç–æ–π —Å–ª–æ–≤–∞—Ä—å –¥–ª—è –≥—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ –ø–æ –∫—É—Ä—Å–∞–º
courses_dict = {}
# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π –∫–æ—Ä—Ç–µ–∂ –≤ —Å–ø–∏—Å–∫–µ students
for name, course in students:
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ —Ç–∞–∫–æ–π –∫—É—Ä—Å –≤ —Å–ª–æ–≤–∞—Ä–µ
    if course not in courses_dict:
    # –ï—Å–ª–∏ –∫—É—Ä—Å–∞ –Ω–µ—Ç ‚Äî —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π –∫–ª—é—á —Å–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º –ø—É—Å—Ç–æ–≥–æ —Å–ø–∏—Å–∫–∞
        courses_dict[course] = []
    # –î–æ–±–∞–≤–ª—è–µ–º –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞ –≤ —Å–ø–∏—Å–æ–∫ –ø–æ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–º—É –∫—É—Ä—Å—É
    courses_dict[course].append(name)
# –í—ã–≤–æ–¥–∏–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å
print(courses_dict)
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# –ú—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ü–∏–∫–ª for –∏ —É—Å–ª–æ–≤–∏–µ if, —á—Ç–æ–±—ã —Å–æ–±—Ä–∞—Ç—å –¥–∞–Ω–Ω—ã–µ –ø–æ –≥—Ä—É–ø–ø–∞–º.
# –ö–∞–∂–¥—ã–π –∫—É—Ä—Å —Å—Ç–∞–Ω–æ–≤–∏—Ç—Å—è –∫–ª—é—á–æ–º, –∞ —Å–ø–∏—Å–æ–∫ –∏–º—ë–Ω ‚Äî –∑–Ω–∞—á–µ–Ω–∏–µ–º.
# –ü–æ—à–∞–≥–æ–≤–æ:
# "Python" ‚Üí —Å–æ–∑–¥–∞—ë–º –∫–ª—é—á "Python" ‚Üí –¥–æ–±–∞–≤–ª—è–µ–º "–ê–Ω–Ω–∞"
# "Java" ‚Üí —Å–æ–∑–¥–∞—ë–º –∫–ª—é—á "Java" ‚Üí –¥–æ–±–∞–≤–ª—è–µ–º "–ë–æ—Ä–∏—Å"
# "Python" ‚Üí –∫–ª—é—á —É–∂–µ –µ—Å—Ç—å ‚Üí –¥–æ–±–∞–≤–ª—è–µ–º "–í–∏–∫—Ç–æ—Ä"
# "C++" ‚Üí —Å–æ–∑–¥–∞—ë–º –∫–ª—é—á ‚Üí –¥–æ–±–∞–≤–ª—è–µ–º "–ì–∞–ª–∏–Ω–∞"
# "Python" ‚Üí –¥–æ–±–∞–≤–ª—è–µ–º "–î–º–∏—Ç—Ä–∏–π"



# # ===============================================
# # 5. –°–æ–∑–¥–∞–π—Ç–µ —Å–ª–æ–≤–∞—Ä—å, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –æ—Ü–µ–Ω–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤:
# # –ò—Å–ø–æ–ª—å–∑—É—è –º–µ—Ç–æ–¥ pop(), —É–¥–∞–ª–∏—Ç–µ —Å—Ç—É–¥–µ–Ω—Ç–∞ —Å —Å–∞–º–æ–π –Ω–∏–∑–∫–æ–π –æ—Ü–µ–Ω–∫–æ–π –∏ –≤—ã–≤–µ–¥–∏—Ç–µ –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å.
# # ===============================================

# –°–æ–∑–¥–∞—ë–º —Å–ª–æ–≤–∞—Ä—å —Å –æ—Ü–µ–Ω–∫–∞–º–∏ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤: –∫–ª—é—á–∏ ‚Äî –∏–º–µ–Ω–∞, –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî –æ—Ü–µ–Ω–∫–∏
grades = {"–ê–Ω–Ω–∞": 5, "–ë–æ—Ä–∏—Å": 4, "–í–∏–∫—Ç–æ—Ä": 3, "–ì–∞–ª–∏–Ω–∞": 5, "–î–º–∏—Ç—Ä–∏–π": 2}
# –ù–∞—Ö–æ–¥–∏–º –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞ —Å –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π –æ—Ü–µ–Ω–∫–æ–π
# min(grades, key=grades.get) ‚Äî –∏—â–µ—Ç –∫–ª—é—á, —É –∫–æ—Ç–æ—Ä–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏–µ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ
student_with_min_grade = min(grades, key=grades.get)
# –£–¥–∞–ª—è–µ–º —ç—Ç–æ–≥–æ —Å—Ç—É–¥–µ–Ω—Ç–∞ –∏–∑ —Å–ª–æ–≤–∞—Ä—è —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ pop()
grades.pop(student_with_min_grade)
# –í—ã–≤–æ–¥–∏–º –æ–±–Ω–æ–≤–ª—ë–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å
print(grades)
# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# min(grades, key=grades.get) –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–ª—é—á (–∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞), —É –∫–æ—Ç–æ—Ä–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏–µ (–æ—Ü–µ–Ω–∫–∞) –º–∏–Ω–∏–º–∞–ª—å–Ω–æ.
# pop(key) —É–¥–∞–ª—è–µ—Ç —ç–ª–µ–º–µ–Ω—Ç –ø–æ –∫–ª—é—á—É –∏ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ) –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏–µ.
# –≠—Ç–æ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –∑–∞–¥–∞–Ω–∏—é: –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å pop() –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è.



