# # 6. –î–∞–Ω —Å–ø–∏—Å–æ–∫ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤:
# # students = ["–ê–Ω–Ω–∞", "–ë–æ—Ä–∏—Å", "–í–∏–∫—Ç–æ—Ä", "–ì–∞–ª–∏–Ω–∞"]
# # –ü—Ä–µ–æ–±—Ä–∞–∑—É–π—Ç–µ —ç—Ç–æ—Ç —Å–ø–∏—Å–æ–∫ –≤ —Å–ª–æ–≤–∞—Ä—å, –∑–∞–ø–æ–ª–Ω–∏–≤ –∑–Ω–∞—á–µ–Ω–∏—è –≤—Å–µ—Ö —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ None. –ó–∞—Ç–µ–º –æ–±–Ω–æ–≤–∏—Ç–µ —Å–ª–æ–≤–∞—Ä—å —É–∫–∞–∑–∞–≤ –≤ –∑–Ω–∞—á–µ–Ω–∏–µ –≤–æ–∑—Ä–∞—Å—Ç —Å—Ç—É–¥–µ–Ω—Ç–æ–≤.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫ –∏–º—ë–Ω —Å—Ç—É–¥–µ–Ω—Ç–æ–≤
students = ["–ê–Ω–Ω–∞", "–ë–æ—Ä–∏—Å", "–í–∏–∫—Ç–æ—Ä", "–ì–∞–ª–∏–Ω–∞"]
# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å–ø–∏—Å–æ–∫ –≤ —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á–∏ ‚Äî –∏–º–µ–Ω–∞, –∞ –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî None
# –ò—Å–ø–æ–ª—å–∑—É–µ–º dict.fromkeys(), –∫–æ—Ç–æ—Ä—ã–π —Å–æ–∑–¥–∞—ë—Ç —Å–ª–æ–≤–∞—Ä—å —Å –æ–¥–∏–Ω–∞–∫–æ–≤—ã–º –∑–Ω–∞—á–µ–Ω–∏–µ–º –¥–ª—è –≤—Å–µ—Ö –∫–ª—é—á–µ–π
