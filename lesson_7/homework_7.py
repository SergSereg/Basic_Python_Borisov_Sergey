# # 4. –í—ã–≤–æ–¥ –≤—Å–µ—Ö —á–∏—Å–µ–ª –æ—Ç A –¥–æ B —Å —à–∞–≥–æ–º K
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è A, B –∏ K. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ for –∏ range() –¥–ª—è –≤—ã–≤–æ–¥–∞ –≤—Å–µ—Ö —á–∏—Å–µ–ª –æ—Ç A –¥–æ B –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ —Å —à–∞–≥–æ–º K.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—á–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ A –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–µ–ª–æ–µ
A = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –ê: "))
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∫–æ–Ω–µ—á–Ω–æ–µ —á–∏—Å–ª–æ B –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–µ–ª–æ–µ
B = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ B: "))
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —à–∞–≥ K –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–µ–ª–æ–µ
K = int(input("–í–≤–µ–¥–∏—Ç–µ —à–∞–≥ K: "))
# –¶–∏–∫–ª for —Å range(): –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º —á–∏—Å–ª–∞ –æ—Ç A –¥–æ B –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ —Å —à–∞–≥–æ–º K
# range(A, B+1, K) ‚Äî –ø–æ—Ç–æ–º—É —á—Ç–æ range –Ω–µ –≤–∫–ª—é—á–∞–µ—Ç –ø—Ä–∞–≤—É—é –≥—Ä–∞–Ω–∏—Ü—É
for num in range(A, B + 1, K):
    # –í—ã–≤–æ–¥–∏–º –∫–∞–∂–¥–æ–µ —á–∏—Å–ª–æ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
    print(num)

