# # ===============================================
# # 1. –í—ã–≤–æ–¥ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ N
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N. –ò—Å–ø–æ–ª—å–∑—É—è —Ü–∏–∫–ª while, –≤—ã–≤–µ–¥–∏—Ç–µ –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ N –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
N = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ N: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—á—ë—Ç—á–∏–∫ i –∑–Ω–∞—á–µ–Ω–∏–µ–º 1 ‚Äî —Å –Ω–µ–≥–æ –Ω–∞—á–Ω—ë–º –≤—ã–≤–æ–¥
i = 1
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ i –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ N ‚Äî –≤—ã–ø–æ–ª–Ω—è–π —Ç–µ–ª–æ —Ü–∏–∫–ª–∞"
# –£—Å–ª–æ–≤–∏–µ i <= N –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ —á–∏—Å–ª–æ N —Ç–æ–∂–µ –±—É–¥–µ—Ç –≤—ã–≤–µ–¥–µ–Ω–æ (–≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ)
while i <= N:
    # –í—ã–≤–æ–¥–∏–º —Ç–µ–∫—É—â–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Å—á—ë—Ç—á–∏–∫–∞ i
    print(i)
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ –Ω–∞ 1, —á—Ç–æ–±—ã –ø–µ—Ä–µ–π—Ç–∏ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —á–∏—Å–ª—É
    # –ë–µ–∑ —ç—Ç–æ–≥–æ —Ü–∏–∫–ª –±—É–¥–µ—Ç –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–º!
    i += 1



# # ===============================================
# # 2. –°—É–º–º–∞ —á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –¥–æ N
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N. –ò—Å–ø–æ–ª—å–∑—É—è —Ü–∏–∫–ª while, –≤—ã—á–∏—Å–ª–∏—Ç–µ —Å—É–º–º—É –≤—Å–µ—Ö —á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ N.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
N = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ N: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –Ω–∞–∫–æ–ø–ª–µ–Ω–∏—è —Å—É–º–º—ã —á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª
sum_even = 0
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—á—ë—Ç—á–∏–∫ i –∑–Ω–∞—á–µ–Ω–∏–µ–º 1 ‚Äî –Ω–∞—á–Ω—ë–º –ø—Ä–æ–≤–µ—Ä–∫—É —Å –ø–µ—Ä–≤–æ–≥–æ —á–∏—Å–ª–∞
i = 1
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ i –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ N ‚Äî –≤—ã–ø–æ–ª–Ω—è–π —Ç–µ–ª–æ —Ü–∏–∫–ª–∞"
while i <= N:
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ i —á—ë—Ç–Ω—ã–º (–æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 2 —Ä–∞–≤–µ–Ω 0
    if i % 2 == 0:
        # –ï—Å–ª–∏ —á–∏—Å–ª–æ —á—ë—Ç–Ω–æ–µ ‚Äî –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –∫ –æ–±—â–µ–π —Å—É–º–º–µ
        sum_even += i
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ –Ω–∞ 1, —á—Ç–æ–±—ã –ø–µ—Ä–µ–π—Ç–∏ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —á–∏—Å–ª—É
    i += 1
# –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—É—é —Å—É–º–º—É –≤—Å–µ—Ö —á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ N
print(sum_even)

# –ö–∞–∫ —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–∞ –ø—Ä–∏–º–µ—Ä–µ N = 6:
# i=1 ‚Üí 1<=6? –î–ê ‚Üí 1%2==0? –ù–ï–¢ ‚Üí i=2
# i=2 ‚Üí 2<=6? –î–ê ‚Üí 2%2==0? –î–ê ‚Üí sum=0+2=2 ‚Üí i=3
# i=3 ‚Üí 3<=6? –î–ê ‚Üí 3%2==0? –ù–ï–¢ ‚Üí i=4
# i=4 ‚Üí 4<=6? –î–ê ‚Üí 4%2==0? –î–ê ‚Üí sum=2+4=6 ‚Üí i=5
# i=5 ‚Üí 5<=6? –î–ê ‚Üí 5%2==0? –ù–ï–¢ ‚Üí i=6
# i=6 ‚Üí 6<=6? –î–ê ‚Üí 6%2==0? –î–ê ‚Üí sum=6+6=12 ‚Üí i=7
# i=7 ‚Üí 7<=6? –ù–ï–¢ ‚Üí –í–´–•–û–î ‚Üí print(12)



# # ===============================================
# # 3. –ü–æ–¥—Å—á–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Ü–∏—Ñ—Ä –≤ —á–∏—Å–ª–µ
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. –ò—Å–ø–æ–ª—å–∑—É—è —Ü–∏–∫–ª while, –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ —Å–∫–æ–ª—å–∫–æ –≤ –Ω–µ–º —Ü–∏—Ñ—Ä.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
number = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—á—ë—Ç—á–∏–∫ —Ü–∏—Ñ—Ä –∑–Ω–∞—á–µ–Ω–∏–µ–º 0
count = 0
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ number –±–æ–ª—å—à–µ 0 ‚Äî –≤—ã–ø–æ–ª–Ω—è–π —Ç–µ–ª–æ —Ü–∏–∫–ª–∞"
# –ù–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ –±—É–¥–µ–º "–æ—Ç–±—Ä–∞—Å—ã–≤–∞—Ç—å" –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É —á–µ—Ä–µ–∑ –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ 10
while number > 0:
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ —Ü–∏—Ñ—Ä –Ω–∞ 1 (–º—ã –Ω–∞—à–ª–∏ –µ—â—ë –æ–¥–Ω—É —Ü–∏—Ñ—Ä—É)
    count += 1
    # –û—Ç–±—Ä–∞—Å—ã–≤–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É —á–∏—Å–ª–∞ —Å –ø–æ–º–æ—â—å—é —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–≥–æ –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 10
    # –ù–∞–ø—Ä–∏–º–µ—Ä: 123 // 10 = 12, 12 // 10 = 1, 1 // 10 = 0
    number = number // 10
# –í—ã–≤–æ–¥–∏–º –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ü–∏—Ñ—Ä
print(count)

# –ü—Ä–∏–º–µ—Ä 1: number = 7
# 7 // 10 = 0 ‚Üí 1 –∏—Ç–µ—Ä–∞—Ü–∏—è ‚Üí count = 1 ‚Üí –≤—ã–≤–æ–¥: 1
# –ü—Ä–∏–º–µ—Ä 2: number = 100
# 100 ‚Üí 10 ‚Üí 1 ‚Üí 0 ‚Üí 3 –∏—Ç–µ—Ä–∞—Ü–∏–∏ ‚Üí count = 3 ‚Üí –≤—ã–≤–æ–¥: 3




# # ===============================================
# # 4. –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Ü–∏—Ñ—Ä—ã –≤ —á–∏—Å–ª–µ
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ. –ò—Å–ø–æ–ª—å–∑—É—è —Ü–∏–∫–ª while, –Ω–∞–π–¥–∏—Ç–µ –Ω–∞–∏–±–æ–ª—å—à—É—é —Ü–∏—Ñ—Ä—É –≤ —á–∏—Å–ª–µ.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
number = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Ü–∏—Ñ—Ä—ã
# –ù–∞—á–∏–Ω–∞–µ–º —Å 0, –ø–æ—Ç–æ–º—É —á—Ç–æ –ª—é–±–∞—è —Ü–∏—Ñ—Ä–∞ (0‚Äì9) –±—É–¥–µ—Ç >= 0
max_digit = 0
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ number –±–æ–ª—å—à–µ 0 ‚Äî –≤—ã–ø–æ–ª–Ω—è–π —Ç–µ–ª–æ —Ü–∏–∫–ª–∞"
# –ù–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ –±—É–¥–µ–º –∏–∑–≤–ª–µ–∫–∞—Ç—å –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É –∏ –æ–±–Ω–æ–≤–ª—è—Ç—å –º–∞–∫—Å–∏–º—É–º
while number > 0:
    # –ü–æ–ª—É—á–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É —á–∏—Å–ª–∞ —Å –ø–æ–º–æ—â—å—é –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ –æ—Å—Ç–∞—Ç–∫–∞ –æ—Ç –¥–µ–ª–µ–Ω–∏—è (%)
    last_digit = number % 10
    # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Ç–µ–∫—É—â—É—é —Ü–∏—Ñ—Ä—É —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π
    if last_digit > max_digit:
        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è —Ü–∏—Ñ—Ä–∞ –±–æ–ª—å—à–µ ‚Äî –æ–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º
        max_digit = last_digit
    # –û—Ç–±—Ä–∞—Å—ã–≤–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É —á–µ—Ä–µ–∑ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ 10
    number = number // 10
# –í—ã–≤–æ–¥–∏–º –Ω–∞–∏–±–æ–ª—å—à—É—é —Ü–∏—Ñ—Ä—É, –Ω–∞–π–¥–µ–Ω–Ω—É—é –≤ —á–∏—Å–ª–µ
print(max_digit)



# # ===============================================
# # 5. –ó–∞–ø—Ä–æ—Å –ø–∞—Ä–æ–ª—è
# # –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∑–∞–ø—Ä–∞—à–∏–≤–∞—Ç—å —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–∞—Ä–æ–ª—å, –ø–æ–∫–∞ –æ–Ω –Ω–µ –≤–≤–µ–¥–µ—Ç –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π ("qwerty123").
# # –ö–∞–∫ —Ç–æ–ª—å–∫–æ –≤–≤–µ–¥–µ–Ω –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–∞—Ä–æ–ª—å, –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ "–î–æ—Å—Ç—É–ø —Ä–∞–∑—Ä–µ—à–µ–Ω".
# # ===============================================

# –ó–∞–¥–∞—ë–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–∞—Ä–æ–ª—å ‚Äî —ç—Ç–∞–ª–æ–Ω –¥–ª—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
correct_password = "qwerty123"
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–µ—Ä–≤—ã–π –≤–≤–æ–¥ –ø–∞—Ä–æ–ª—è
input_password = input("–í–≤–µ–¥–∏—Ç–µ –ø–∞—Ä–æ–ª—å: ")
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ –≤–≤–µ–¥—ë–Ω–Ω—ã–π –ø–∞—Ä–æ–ª—å –ù–ï —Ä–∞–≤–µ–Ω –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º—É ‚Äî –ø–æ–≤—Ç–æ—Ä—è—Ç—å"
while input_password != correct_password:
    # –ï—Å–ª–∏ –ø–∞—Ä–æ–ª—å –Ω–µ–≤–µ—Ä–Ω—ã–π ‚Äî —Å–Ω–æ–≤–∞ –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –µ–≥–æ
    input_password = input("–í–≤–µ–¥–∏—Ç–µ –ø–∞—Ä–æ–ª—å: ")
    # –ö–∞–∫ —Ç–æ–ª—å–∫–æ —Ü–∏–∫–ª –∑–∞–≤–µ—Ä—à–∏–ª—Å—è (–ø–∞—Ä–æ–ª–∏ —Å–æ–≤–ø–∞–ª–∏) ‚Äî –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± —É—Å–ø–µ—Ö–µ
print("–î–æ—Å—Ç—É–ø —Ä–∞–∑—Ä–µ—à–µ–Ω")



# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # –ó–∞–¥–∞—á–∏ –ø–æ —É—Ä–æ–∫—É "–û–ø–µ—Ä–∞—Ç–æ—Ä—ã break, continue –∏ else –≤ —Ü–∏–∫–ª–µ while"
# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # ===============================================
# # ===============================================
# # 1. –ü–æ–∏—Å–∫ –ø–µ—Ä–≤–æ–≥–æ —á–µ—Ç–Ω–æ–≥–æ —á–∏—Å–ª–∞
# # –ü—É—Å—Ç—å —É –Ω–∞—Å –µ—Å—Ç—å —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª. –ò—Å–ø–æ–ª—å–∑—É—è —Ü–∏–∫–ª while, –Ω–∞–π–¥–∏—Ç–µ –ø–µ—Ä–≤–æ–µ —á–µ—Ç–Ω–æ–µ —á–∏—Å–ª–æ –∏ –æ—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ —Ü–∏–∫–ª.
# # –ï—Å–ª–∏ —á–µ—Ç–Ω–æ–µ —á–∏—Å–ª–æ –Ω–∞–π–¥–µ–Ω–æ, –≤—ã–≤–µ–¥–∏—Ç–µ –µ–≥–æ, –∏–Ω–∞—á–µ ‚Äì "–ß–µ—Ç–Ω–æ–µ —á–∏—Å–ª–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ".
# # ===============================================

# –ó–∞–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª (–º–æ–∂–Ω–æ –∑–∞–º–µ–Ω–∏—Ç—å –Ω–∞ –ª—é–±–æ–π –¥—Ä—É–≥–æ–π)
numbers = [1, 3, 5, 8, 9, 11]
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –∏–Ω–¥–µ–∫—Å –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ —Å–ø–∏—Å–∫–∞
i = 0
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
found_even = None
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ –Ω–µ –¥–æ—Å—Ç–∏–≥–ª–∏ –∫–æ–Ω—Ü–∞ —Å–ø–∏—Å–∫–∞"
while i < len(numbers):
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ —á—ë—Ç–Ω—ã–º
    if numbers[i] % 2 == 0:
        # –ï—Å–ª–∏ –¥–∞ ‚Äî —Å–æ—Ö—Ä–∞–Ω—è–µ–º –µ–≥–æ –∏ –ø—Ä–µ—Ä—ã–≤–∞–µ–º —Ü–∏–∫–ª
        found_even = numbers[i]
        # –Ω–µ–º–µ–¥–ª–µ–Ω–Ω–æ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
        break
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∏–Ω–¥–µ–∫—Å, —á—Ç–æ–±—ã –ø–µ—Ä–µ–π—Ç–∏ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —ç–ª–µ–º–µ–Ω—Ç—É
    i += 1
# –ü–æ—Å–ª–µ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Ü–∏–∫–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª–æ –ª–∏ –Ω–∞–π–¥–µ–Ω–æ —á—ë—Ç–Ω–æ–µ —á–∏—Å–ª–æ
if found_even is not None:
    # –ï—Å–ª–∏ –Ω–∞–π–¥–µ–Ω–æ ‚Äî –≤—ã–≤–æ–¥–∏–º –µ–≥–æ
    print(found_even)
else:
    # –ï—Å–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ ‚Äî –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    print("–ß–µ—Ç–Ω–æ–µ —á–∏—Å–ª–æ  –Ω–µ –Ω–∞–π–¥–µ–Ω–æ")



# # ===============================================
# # 2. –ü—Ä–æ–ø—É—Å–∫ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª
# # –ó–∞–ø—Ä–∞—à–∏–≤–∞–π—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–∞, –ø–æ–∫–∞ –æ–Ω –Ω–µ –≤–≤–µ–¥–µ—Ç 0. –°—É–º–º–∏—Ä—É–π—Ç–µ —Ç–æ–ª—å–∫–æ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–µ —á–∏—Å–ª–∞.
# # –ï—Å–ª–∏ —á–∏—Å–ª–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ, –ø—Ä–æ–ø—É—Å–∫–∞–π—Ç–µ –µ–≥–æ —Å –ø–æ–º–æ—â—å—é continue.
# # ===============================================

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –Ω–∞–∫–æ–ø–ª–µ–Ω–∏—è —Å—É–º–º—ã –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª
total = 0
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –≤–≤–µ–¥—ë—Ç 0 ‚Äî –ø—Ä–æ–¥–æ–ª–∂–∞—Ç—å"
while True:
    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
    num = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ (0 –¥–ª—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è): "))
    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤—ë–ª 0 ‚Äî –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
    if num == 0:
        break
    # –ï—Å–ª–∏ —á–∏—Å–ª–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ ‚Äî –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –æ—Å—Ç–∞–≤—à—É—é—Å—è —á–∞—Å—Ç—å —Ü–∏–∫–ª–∞
    if num < 0:
        continue # –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏
    # –ï—Å–ª–∏ —á–∏—Å–ª–æ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–µ ‚Äî –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –∫ —Å—É–º–º–µ
    total += num
# –ü–æ—Å–ª–µ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Ü–∏–∫–ª–∞ –≤—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—É—é —Å—É–º–º—É
print(total)



# # ===============================================
# # 3. –í—ã–≤–æ–¥ –Ω–µ—á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –∏–∑ –¥–∏–∞–ø–∞–∑–æ–Ω–∞
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –¥–≤–∞ —á–∏—Å–ª–∞ a –∏ b. –ò—Å–ø–æ–ª—å–∑—É—è while, –≤—ã–≤–µ–¥–∏—Ç–µ –≤—Å–µ –Ω–µ—á–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ [a, b].
# # –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ continue, —á—Ç–æ–±—ã –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å —á–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞—á–∞–ª–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
a = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –∞: "))
# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∫–æ–Ω–µ—Ü –¥–∏–∞–ø–∞–∑–æ–Ω–∞ –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
b = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ b: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ –∑–Ω–∞—á–µ–Ω–∏–µ–º a
current = a
# –¶–∏–∫–ª while: "–ø–æ–∫–∞ current –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ b ‚Äî –≤—ã–ø–æ–ª–Ω—è–π —Ç–µ–ª–æ —Ü–∏–∫–ª–∞"
while current <= b:
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ —á—ë—Ç–Ω—ã–º
    if current % 2 == 0:
        # –ï—Å–ª–∏ —á—ë—Ç–Ω–æ–µ ‚Äî –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –µ–≥–æ –∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —á–∏—Å–ª—É
        current += 1
        continue    # –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å –æ—Å—Ç–∞–≤—à—É—é—Å—è —á–∞—Å—Ç—å —Ü–∏–∫–ª–∞
    # –ï—Å–ª–∏ —á–∏—Å–ª–æ –Ω–µ—á—ë—Ç–Ω–æ–µ ‚Äî –≤—ã–≤–æ–¥–∏–º –µ–≥–æ
    print(current)
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Ç–µ–∫—É—â–µ–µ —á–∏—Å–ª–æ –Ω–∞ 1
    current += 1



# # ===============================================
# # 4. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø—Ä–æ—Å—Ç–æ–µ —á–∏—Å–ª–æ
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N. –û–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –æ–Ω–æ –ø—Ä–æ—Å—Ç—ã–º (–¥–µ–ª–∏—Ç—Å—è —Ç–æ–ª—å–∫–æ –Ω–∞ 1 –∏ —Å–∞–º–æ —Å–µ–±—è).
# # –ï—Å–ª–∏ –Ω–∞—à–ª–∏ –¥–µ–ª–∏—Ç–µ–ª—å, –æ—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ —Ü–∏–∫–ª (break).
# # –ï—Å–ª–∏ break –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ else, —á—Ç–æ–±—ã —Å–æ–æ–±—â–∏—Ç—å, —á—Ç–æ —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
N = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ N: "))
# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—á—ë—Ç—á–∏–∫ –¥–µ–ª–∏—Ç–µ–ª–µ–π
divisor_found = False
# –ï—Å–ª–∏ —á–∏—Å–ª–æ –º–µ–Ω—å—à–µ 2, –æ–Ω–æ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –ø—Ä–æ—Å—Ç—ã–º
if N < 2:
    print("–ß–∏—Å–ª–æ –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –ø—Ä–æ—Å—Ç—ã–º")
else:
    # –¶–∏–∫–ª while: –ø—Ä–æ–≤–µ—Ä—è–µ–º –≤–æ–∑–º–æ–∂–Ω—ã–µ –¥–µ–ª–∏—Ç–µ–ª–∏ –æ—Ç 2 –¥–æ N-1
    i = 2
    while i < N:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –¥–µ–ª–∏—Ç—Å—è –ª–∏ N –Ω–∞ i –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞
        if N % i == 0:
            # –ï—Å–ª–∏ –¥–µ–ª–∏—Ç–µ–ª—å –Ω–∞–π–¥–µ–Ω ‚Äî —á–∏—Å–ª–æ –Ω–µ –ø—Ä–æ—Å—Ç–æ–µ
            divisor_found = True
            break # –Ω–µ–º–µ–¥–ª–µ–Ω–Ω–æ –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
        i += 1
    # –ë–ª–æ–∫ else —Ü–∏–∫–ª–∞ while: –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, –¢–û–õ–¨–ö–û –µ—Å–ª–∏ —Ü–∏–∫–ª –∑–∞–≤–µ—Ä—à–∏–ª—Å—è –±–µ–∑ break
    else:
        # –ï—Å–ª–∏ break –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª ‚Äî –¥–µ–ª–∏—Ç–µ–ª–µ–π –Ω–µ –Ω–∞–π–¥–µ–Ω–æ ‚Üí —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ
        print("–ß–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ")
    # –ï—Å–ª–∏ break —Å—Ä–∞–±–æ—Ç–∞–ª (divisor_found = True), –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    if divisor_found:
        print("–ß–∏—Å–ª–æ –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –ø—Ä–æ—Å—Ç—ã–º")



# # ===============================================
# # 5. –ü–æ–∏—Å–∫ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —á–∏—Å–ª–∞
# # –ó–∞–ø—Ä–∞—à–∏–≤–∞–π—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–∞, –ø–æ–∫–∞ –æ–Ω –Ω–µ –≤–≤–µ–¥–µ—Ç 0. –ù–∞–π–¥–∏—Ç–µ –Ω–∞–∏–±–æ–ª—å—à–µ–µ –≤–≤–µ–¥–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ.
# # –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ break, –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –æ—Ç–∫–∞–∂–µ—Ç—Å—è –≤–≤–æ–¥–∏—Ç—å –¥–∞–Ω–Ω—ã–µ (–≤–≤–µ–¥–µ—Ç –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É).
# # ===============================================

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —á–∏—Å–ª–∞
# –ù–∞—á–∏–Ω–∞–µ–º —Å None, –ø–æ—Ç–æ–º—É —á—Ç–æ –µ—â—ë –Ω–µ –≤–≤–µ–¥–µ–Ω–æ –Ω–∏ –æ–¥–Ω–æ–≥–æ —á–∏—Å–ª–∞
max_number = None
# –¶–∏–∫–ª while: –±–µ—Å–∫–æ–Ω–µ—á–Ω—ã–π —Ü–∏–∫–ª, —É–ø—Ä–∞–≤–ª—è–µ–º—ã–π break
while True:
    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏ (–Ω–µ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ä–∞–∑—É –≤ int!)
    user_input = input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ(0 –¥–ª—è –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è): ")
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –≤–≤—ë–ª –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É (–æ—Ç–∫–∞–∑ –æ—Ç –≤–≤–æ–¥–∞)
    if user_input == "":
        # –ï—Å–ª–∏ –≤–≤–µ–¥–µ–Ω–∞ –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ ‚Äî –ø—Ä–µ—Ä—ã–≤–∞–µ–º —Ü–∏–∫–ª
        break
    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤–≤–µ–¥—ë–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –≤ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
    num = int(user_input)
    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤—ë–ª 0 ‚Äî –∑–∞–≤–µ—Ä—à–∞–µ–º –≤–≤–æ–¥
    if num == 0:
        break
    # –ï—Å–ª–∏ —ç—Ç–æ –ø–µ—Ä–≤–æ–µ –≤–≤–µ–¥—ë–Ω–Ω–æ–µ —á–∏—Å–ª–æ (max_number –µ—â—ë None)
    if max_number is None:
        # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –µ–≥–æ –∫–∞–∫ —Ç–µ–∫—É—â–∏–π –º–∞–∫—Å–∏–º—É–º
        max_number = num
    else:
        # –ò–Ω–∞—á–µ —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º —Å —Ç–µ–∫—É—â–∏–º –º–∞–∫—Å–∏–º—É–º–æ–º
        if num > max_number:
            # –ï—Å–ª–∏ –Ω–æ–≤–æ–µ —á–∏—Å–ª–æ –±–æ–ª—å—à–µ ‚Äî –æ–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º
            max_number = num
# –ü–æ—Å–ª–µ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Ü–∏–∫–ª–∞ –ø—Ä–æ–≤–µ—Ä—è–µ–º, –±—ã–ª–∏ –ª–∏ –≤–≤–µ–¥–µ–Ω—ã —á–∏—Å–ª–∞
if max_number is not None:
    # –ï—Å–ª–∏ –¥–∞ ‚Äî –≤—ã–≤–æ–¥–∏–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ
    print(max_number)
else:
    # –ï—Å–ª–∏ –Ω–µ—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Å—Ä–∞–∑—É –≤–≤–µ–ª–∏ 0 –∏–ª–∏ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É) ‚Äî –Ω–∏—á–µ–≥–æ –Ω–µ –≤—ã–≤–æ–¥–∏–º
    # (–ø–æ –∑–∞–¥–∞–Ω–∏—é –Ω–µ —Ç—Ä–µ–±—É–µ—Ç—Å—è —Å–æ–æ–±—â–µ–Ω–∏–µ, –ø–æ—ç—Ç–æ–º—É –ø—Ä–æ—Å—Ç–æ –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º)
    pass



# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # –ó–∞–¥–∞—á–∏ –ø–æ —É—Ä–æ–∫—É "–¶–∏–∫–ª for –≤ Python ‚Äì –æ—Å–Ω–æ–≤—ã –∏ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–µ"
# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # ===============================================
# # 1. –í—ã–≤–µ—Å—Ç–∏ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã —Å—Ç—Ä–æ–∫–∏ –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å—Ç—Ä–æ–∫—É –∏ —Å –ø–æ–º–æ—â—å—é for –≤—ã–≤–µ–¥–∏—Ç–µ –µ–µ —Å–∏–º–≤–æ–ª—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.
# # (–í—Å–ø–æ–º–Ω–∏—Ç–µ –º–µ—Ç–æ–¥—ã —Å–ø–∏—Å–∫–∞)
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å—Ç—Ä–æ–∫—É
user_string = input("–í–≤–µ–¥–∏—Ç–µ —Å—Ç—Ä–æ–∫—É: ")
# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Å–ø–∏—Å–æ–∫ —Å–∏–º–≤–æ–ª–æ–≤ –∏ —Ä–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –µ–≥–æ —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ reverse()
char_list = list(user_string)  # —Å–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ –∏–∑ —Å–∏–º–≤–æ–ª–æ–≤ —Å—Ç—Ä–æ–∫–∏
char_list.reverse()            # –∏–∑–º–µ–Ω—è–µ–º —Å–ø–∏—Å–æ–∫ "–Ω–∞ –º–µ—Å—Ç–µ", —Ä–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞—è –µ–≥–æ
# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª –≤ —Ä–∞–∑–≤—ë—Ä–Ω—É—Ç–æ–º —Å–ø–∏—Å–∫–µ —Å –ø–æ–º–æ—â—å—é —Ü–∏–∫–ª–∞ for
for char in char_list:
    # –í—ã–≤–æ–¥–∏–º –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
    print(char)



