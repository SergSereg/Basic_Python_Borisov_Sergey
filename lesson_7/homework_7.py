# # 3. –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å–ø–∏—Å–∫–∞ —Å—Ç–µ–ø–µ–Ω–µ–π –¥–≤–æ–π–∫–∏
# # –ó–∞–ø—Ä–æ—Å–∏—Ç–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N. –°—Ñ–æ—Ä–º–∏—Ä—É–π—Ç–µ —Å–ø–∏—Å–æ–∫ —Å—Ç–µ–ø–µ–Ω–µ–π –¥–≤–æ–π–∫–∏ –æ—Ç 2^0 –¥–æ 2^N –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ.
# # ===============================================

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–∏—Å–ª–æ N –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –µ–≥–æ –≤ —Ü–µ–ª–æ–µ
N = int(input("–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ N: "))
# –°–æ–∑–¥–∞—ë–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å—Ç–µ–ø–µ–Ω–µ–π –¥–≤–æ–π–∫–∏
powers_of_two = []
# –¶–∏–∫–ª for —Å range(): –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º –ø–æ–∫–∞–∑–∞—Ç–µ–ª–∏ —Å—Ç–µ–ø–µ–Ω–∏ –æ—Ç 0 –¥–æ N –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ
for i in range(N + 1): # range(N+1) –¥–∞—ë—Ç 0, 1, 2, ..., N
    # –í—ã—á–∏—Å–ª—è–µ–º 2 –≤ —Å—Ç–µ–ø–µ–Ω–∏ i –∏ –¥–æ–±–∞–≤–ª—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ —Å–ø–∏—Å–æ–∫
    powers_of_two.append(2 ** i)
# –í—ã–≤–æ–¥–∏–º –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫
print(powers_of_two)

