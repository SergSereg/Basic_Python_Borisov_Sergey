# –∞ 2^6 = 64, –ø–æ—Ç–æ–º—É —á—Ç–æ 2 —É–º–Ω–æ–∂–∞–µ—Ç—Å—è –Ω–∞ —Å–µ–±—è 6 —Ä–∞–∑.
