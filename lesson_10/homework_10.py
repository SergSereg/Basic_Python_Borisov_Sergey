# # –ß–∏—Å–ª–æ 5 (–µ—Å–ª–∏ –æ–Ω–æ –µ—Å—Ç—å).
# # –ß–∏—Å–ª–æ 15 (–∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –º–µ—Ç–æ–¥, –∫–æ—Ç–æ—Ä—ã–π –Ω–µ –≤—ã–∑–æ–≤–µ—Ç –æ—à–∏–±–∫—É).
