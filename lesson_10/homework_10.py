# # 2. –°–æ–∑–¥–∞–π—Ç–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏–∑ —Å–ø–∏—Å–∫–∞ –≥–æ—Ä–æ–¥–æ–≤:
