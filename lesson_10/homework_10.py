# –£–¥–∞–ª—è–µ–º —á–∏—Å–ª–æ 15 ‚Äî –µ–≥–æ –Ω–µ—Ç –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ
# –ò—Å–ø–æ–ª—å–∑—É–µ–º .discard(), –ø–æ—Ç–æ–º—É —á—Ç–æ –æ–Ω –ù–ï –≤—ã–∑—ã–≤–∞–µ—Ç –æ—à–∏–±–∫—É, –µ—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç–∞ –Ω–µ—Ç
numbers_set.discard(15) # .remove(15) ‚Äî —É–¥–∞–ª—è–µ—Ç 15, –Ω–æ –≤—ã–∑–æ–≤–µ—Ç –æ—à–∏–±–∫—É, –µ—Å–ª–∏ 15 –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç.

print(numbers_set)



# # ===============================================
# # 4. –ü—Ä–µ–æ–±—Ä–∞–∑—É–π—Ç–µ —Å—Ç—Ä–æ–∫—É "abrakadabra" –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤.
# # –í—ã–≤–µ–¥–∏—Ç–µ –∏—Ç–æ–≥–æ–≤–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, —Å–∫–æ–ª—å–∫–æ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –±—É–∫–≤ –≤ —Å–ª–æ–≤–µ.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
word = "abrakadabra"
# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ ‚Äî –∫–∞–∂–¥—ã–π —Å–∏–º–≤–æ–ª —Å—Ç–∞–Ω–æ–≤–∏—Ç—Å—è —ç–ª–µ–º–µ–Ω—Ç–æ–º –º–Ω–æ–∂–µ—Å—Ç–≤–∞,
# –∞ –¥—É–±–ª–∏–∫–∞—Ç—ã –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —É–¥–∞–ª—è—é—Ç—Å—è
unique_letters = set(word)

# –í—ã–≤–æ–¥–∏–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –±—É–∫–≤
print(unique_letters)

# –í—ã–≤–æ–¥–∏–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –±—É–∫–≤ —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ len()
print(len(unique_letters))



# # ===============================================
# # 5. –°–æ–∑–¥–∞–π—Ç–µ –ø—É—Å—Ç–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏ –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –¥–æ–±–∞–≤–∏—Ç—å –≤ –Ω–µ–≥–æ —Å–ª–µ–¥—É—é—â–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã:
# # –ß–∏—Å–ª–æ 10
# # –°—Ç—Ä–æ–∫—É "hello"
# # –ö–æ—Ä—Ç–µ–∂ (1, 2, 3)
# # –°–ø–∏—Å–æ–∫ [4, 5, 6]
# # –ö–∞–∫–∏–µ –∏–∑ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–µ–ª—å–∑—è –¥–æ–±–∞–≤–∏—Ç—å –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ? –ü–æ—á–µ–º—É?
# # ===============================================

# –°–æ–∑–¥–∞—ë–º –ø—É—Å—Ç–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ (–æ–±—è–∑–∞—Ç–µ–ª—å–Ω–æ —á–µ—Ä–µ–∑ set(), –∞ –Ω–µ {})
my_set = set()
# –î–æ–±–∞–≤–ª—è–µ–º —á–∏—Å–ª–æ 10 ‚Äî –º–æ–∂–Ω–æ, –ø–æ—Ç–æ–º—É —á—Ç–æ int ‚Äî –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—ã–π —Ç–∏–ø
my_set.add(10)
# –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É "hello" ‚Äî –º–æ–∂–Ω–æ, str ‚Äî –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—ã–π
my_set.add("Hello")
# –î–æ–±–∞–≤–ª—è–µ–º –∫–æ—Ä—Ç–µ–∂ (1, 2, 3) ‚Äî –º–æ–∂–Ω–æ, tuple ‚Äî –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—ã–π (–µ—Å–ª–∏ —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã)
my_set.add((1, 2, 3))

# my_set.add([4, 5, 6])  # ‚Üê –í—ã–∑–æ–≤–µ—Ç TypeError!
# –°–ø–∏—Å–æ–∫ [4, 5, 6] –Ω–µ–ª—å–∑—è –¥–æ–±–∞–≤–∏—Ç—å –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–æ,
# –ø–æ—Ç–æ–º—É —á—Ç–æ —Å–ø–∏—Å–æ–∫ ‚Äî –∏–∑–º–µ–Ω—è–µ–º—ã–π –æ–±—ä–µ–∫—Ç (mutable),
# –∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –º–æ–≥—É—Ç —Å–æ–¥–µ—Ä–∂–∞—Ç—å –¢–û–õ–¨–ö–û –Ω–µ–∏–∑–º–µ–Ω—è–µ–º—ã–µ (hashable) –æ–±—ä–µ–∫—Ç—ã.
print(my_set)



# # ===============================================
# # 6. –°–æ–∑–¥–∞–π—Ç–µ 2 –º–Ω–æ–∂–µ—Å—Ç–≤–∞, –≤ –∫–æ—Ç–æ—Ä—ã—Ö —Å–æ–≤–ø–∞–¥–∞—é—Ç 2 —ç–ª–µ–º–µ–Ω—Ç–∞
# # –ù–∞–π–¥–∏—Ç–µ:
# # –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ A –∏ B.
# # –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ A –∏ B.
# # –†–∞–∑–Ω–æ—Å—Ç—å A - B.
# # –†–∞–∑–Ω–æ—Å—Ç—å B - A.
# # –°–∏–º–º–µ—Ç—Ä–∏—á–Ω—É—é —Ä–∞–∑–Ω–æ—Å—Ç—å A ^ B.
# # ===============================================

# –°–æ–∑–¥–∞—ë–º –¥–≤–∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å –¥–≤—É–º—è –æ–±—â–∏–º–∏ —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏: 3 –∏ 4
A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

# –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ: —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –∏ –≤ A, –∏ –≤ B
intersection = A & B  # –∏–ª–∏ A.intersection(B)
print("–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ A –∏ B:", intersection)

# –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ: –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –∏–∑ A –∏ B –±–µ–∑ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤
union = A | B  # –∏–ª–∏ A.union(B)
print("–û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ A –∏ B:", union)

# –†–∞–∑–Ω–æ—Å—Ç—å A - B: —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ A, –Ω–æ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤ B
diff_A_B = A - B  # –∏–ª–∏ A.difference(B)
print("–†–∞–∑–Ω–æ—Å—Ç—å A - B:", diff_A_B)

# –†–∞–∑–Ω–æ—Å—Ç—å B - A: —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ B, –Ω–æ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –≤ A
diff_B_A = B - A  # –∏–ª–∏ B.difference(A)
print("–†–∞–∑–Ω–æ—Å—Ç—å B - A:", diff_B_A)

# –°–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å: —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –≤ A –∏–ª–∏ —Ç–æ–ª—å–∫–æ –≤ B (–Ω–æ –Ω–µ –≤ –æ–±–æ–∏—Ö)
sym_diff = A ^ B  # –∏–ª–∏ A.symmetric_difference(B)
print("–°–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å A ^ B:", sym_diff)

# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# & ‚Üí –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ
# | ‚Üí –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ
# - ‚Üí —Ä–∞–∑–Ω–æ—Å—Ç—å
# ^ ‚Üí —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å
# –í—Å–µ –æ–ø–µ—Ä–∞—Ü–∏–∏ —Ä–∞–±–æ—Ç–∞—é—Ç —Ç–æ–ª—å–∫–æ —Å –º–Ω–æ–∂–µ—Å—Ç–≤–∞–º–∏.



# # ===============================================
# # 7. –°–æ–∑–¥–∞–π—Ç–µ –¥–≤–∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –∏–∑ —á–∏—Å–µ–ª 1 –¥–æ 10:
# # even_numbers ‚Äî —Å–æ–¥–µ—Ä–∂–∏—Ç —á–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞.
# # odd_numbers ‚Äî —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ—á–µ—Ç–Ω—ã–µ —á–∏—Å–ª–∞.
# # –ü—Ä–æ–≤–µ—Ä—å—Ç–µ:
# # –ö–∞–∫–æ–µ –±—É–¥–µ—Ç –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ &?
# # –ö–∞–∫–æ–µ –±—É–¥–µ—Ç –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ |?
# # ===============================================

# –°–æ–∑–¥–∞—ë–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ —á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10 —Å –ø–æ–º–æ—â—å—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞
even_numbers = {x for x in range(1, 11) if x % 2 == 0}
# –°–æ–∑–¥–∞—ë–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ –Ω–µ—á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10
add_numbers = {x for x in range(1, 11) if x % 2 != 0}

# –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ: —ç–ª–µ–º–µ–Ω—Ç—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –∏ –≤ —á—ë—Ç–Ω—ã—Ö, –∏ –≤ –Ω–µ—á—ë—Ç–Ω—ã—Ö
intersection = even_numbers & add_numbers
print("–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ (&):", intersection)
# –û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ: –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 10 (—á—ë—Ç–Ω—ã–µ + –Ω–µ—á—ë—Ç–Ω—ã–µ)
union = even_numbers | add_numbers
print("–û–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ (|):", union)

# üí° –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ:
# –ß—ë—Ç–Ω—ã–µ –∏ –Ω–µ—á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –Ω–µ –ø–µ—Ä–µ—Å–µ–∫–∞—é—Ç—Å—è ‚Üí –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –±—É–¥–µ—Ç –ø—É—Å—Ç—ã–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ–º.
# –ò—Ö –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –¥–∞—ë—Ç –≤—Å–µ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 10.
# –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ (&): {} (–ø—É—Å—Ç–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ)
# ‚Üí –ø–æ—Ç–æ–º—É —á—Ç–æ —á–∏—Å–ª–æ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ —á—ë—Ç–Ω—ã–º –∏ –Ω–µ—á—ë—Ç–Ω—ã–º.
