# # python_students = {"–ê–Ω–Ω–∞", "–ò–≤–∞–Ω", "–ú–∞—Ä–∏—è", "–°–µ—Ä–≥–µ–π"}
# # java_students = {"–ò–≤–∞–Ω", "–î–º–∏—Ç—Ä–∏–π", "–°–µ—Ä–≥–µ–π", "–ê–ª–µ–∫—Å–µ–π"}
# # –û–ø—Ä–µ–¥–µ–ª–∏—Ç–µ:
# # –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω –Ω–∞ –æ–±–∞ –∫—É—Ä—Å–∞?
# # –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω —Ç–æ–ª—å–∫–æ –Ω–∞ –æ–¥–∏–Ω –∫—É—Ä—Å?
# # –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω —Ö–æ—Ç—è –±—ã –Ω–∞ –æ–¥–∏–Ω –∫—É—Ä—Å?
# # ===============================================

# –ú–Ω–æ–∂–µ—Å—Ç–≤–æ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –∑–∞–ø–∏—Å–∞–≤—à–∏—Ö—Å—è –Ω–∞ –∫—É—Ä—Å Python
python_students = {"–ê–Ω–Ω–∞", "–ò–≤–∞–Ω", "–ú–∞—Ä–∏—è", "–°–µ—Ä–≥–µ–π"}
# –ú–Ω–æ–∂–µ—Å—Ç–≤–æ —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –∑–∞–ø–∏—Å–∞–≤—à–∏—Ö—Å—è –Ω–∞ –∫—É—Ä—Å Java
java_students = {"–ò–≤–∞–Ω", "–î–º–∏—Ç—Ä–∏–π", "–°–µ—Ä–≥–µ–π", "–ê–ª–µ–∫—Å–µ–π"}

# 1. –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω –Ω–∞ –æ–±–∞ –∫—É—Ä—Å–∞? ‚Üí –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤
both_courses = python_students & java_students
print("–ó–∞–ø–∏—Å–∞–Ω—ã –Ω–∞ –æ–±–∞ –∫—É—Ä—Å–∞:", both_courses)

# 2. –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω —Ç–æ–ª—å–∫–æ –Ω–∞ –æ–¥–∏–Ω –∫—É—Ä—Å? ‚Üí —Å–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å
only_one_course = python_students ^ java_students
print("–ó–∞–ø–∏—Å–∞–Ω—ã —Ç–æ–ª—å–∫–æ –Ω–∞ –æ–¥–∏–Ω –∫—É—Ä—Å:", only_one_course)

# 3. –ö—Ç–æ –∑–∞–ø–∏—Å–∞–Ω —Ö–æ—Ç—è –±—ã –Ω–∞ –æ–¥–∏–Ω –∫—É—Ä—Å? ‚Üí –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤
at_least_one = python_students | java_students
print("–ó–∞–ø–∏—Å–∞–Ω—ã —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –∫—É—Ä—Å:", at_least_one)

# & ‚Äî –æ–ø–µ—Ä–∞—Ç–æ—Ä –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è (–∏ –≤ Python, –∏ –≤ Java)
# ^ ‚Äî –æ–ø–µ—Ä–∞—Ç–æ—Ä —Å–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–æ–π —Ä–∞–∑–Ω–æ—Å—Ç–∏ (—Ç–æ–ª—å–∫–æ –≤ –æ–¥–Ω–æ–º –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤)
# | ‚Äî –æ–ø–µ—Ä–∞—Ç–æ—Ä –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏—è (–≤—Å–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã –∏–∑ –æ–±–æ–∏—Ö –º–Ω–æ–∂–µ—Å—Ç–≤)



# # ===============================================
# # 9. –î–∞–Ω—ã –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å–ª–æ–≤:
# # text1 = set("–ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ")
# # text2 = set("–∞–≤—Ç–æ–º–∞—Ç–∏–∑–∞—Ü–∏—è")
# # –ù–∞–π–¥–∏—Ç–µ –æ–±—â–∏–µ –±—É–∫–≤—ã –≤ –¥–≤—É—Ö —Å–ª–æ–≤–∞—Ö.
# # –ù–∞–π–¥–∏—Ç–µ –±—É–∫–≤—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –≤ –ø–µ—Ä–≤–æ–º —Å–ª–æ–≤–µ.
# # –ù–∞–π–¥–∏—Ç–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –±—É–∫–≤—ã –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞.
# # ===============================================

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫–∏ –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å–∏–º–≤–æ–ª–æ–≤ (–±—É–∫–≤)
# set("–ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ") ‚Üí –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –±—É–∫–≤ —Å–ª–æ–≤–∞ "–ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ"
text1 = set("–ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ")

# set("–∞–≤—Ç–æ–º–∞—Ç–∏–∑–∞—Ü–∏—è") ‚Üí –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö –±—É–∫–≤ —Å–ª–æ–≤–∞ "–∞–≤—Ç–æ–º–∞—Ç–∏–∑–∞—Ü–∏—è"
text2 = set("–∞–≤—Ç–æ–º–∞—Ç–∏–∑–∞—Ü–∏—è")

# 1. –û–±—â–∏–µ –±—É–∫–≤—ã –≤ –¥–≤—É—Ö —Å–ª–æ–≤–∞—Ö ‚Üí –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤
common_letters = text1 & text2
print("–û–±—â–∏–µ –±—É–∫–≤—ã:", common_letters)

# 2. –ë—É–∫–≤—ã, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ –≤ –ø–µ—Ä–≤–æ–º —Å–ª–æ–≤–µ ‚Üí —Ä–∞–∑–Ω–æ—Å—Ç—å –º–Ω–æ–∂–µ—Å—Ç–≤
only_in_firts = text1 - text2
print("–¢–æ–ª—å–∫–æ –≤ –ø–µ—Ä–≤–æ–º —Å–ª–æ–≤–µ:", only_in_firts)

# 3. –£–Ω–∏–∫–∞–ª—å–Ω—ã–µ –±—É–∫–≤—ã –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞ ‚Üí —Å–∏–º–º–µ—Ç—Ä–∏—á–µ—Å–∫–∞—è —Ä–∞–∑–Ω–æ—Å—Ç—å
unique_letters = text1 ^ text2
print("–£–Ω–∏–∫–∞–ª—å–Ω—ã–µ –±—É–∫–≤—ã –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞:", unique_letters)

# set(—Å—Ç—Ä–æ–∫–∞) –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —Ä–∞–∑–±–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã (–±–µ–∑ –ø–æ–≤—Ç–æ—Ä–æ–≤).
# "–º" –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è –¥–≤–∞–∂–¥—ã –≤ ¬´–ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ¬ª, –Ω–æ –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ –±—É–¥–µ—Ç —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω —Ä–∞–∑.
# –í—Å–µ –æ–ø–µ—Ä–∞—Ü–∏–∏ (&, -, ^) —Ä–∞–±–æ—Ç–∞—é—Ç –º–≥–Ω–æ–≤–µ–Ω–Ω–æ –∏ –±–µ–∑ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤.



# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä—ã –º–Ω–æ–∂–µ—Å—Ç–≤ –∏ —Å–ª–æ–≤–∞—Ä–µ–π
# # ++++++++++++++++++++++++++++++++++++++++++++++++
# # ===============================================
# # 1. –°–æ–∑–¥–∞–π—Ç–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏–∑ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10, –Ω–æ —Ç–æ–ª—å–∫–æ –¥–ª—è —á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª.
# # ===============================================

# –°–æ–∑–¥–∞—ë–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ —Å –ø–æ–º–æ—â—å—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ –º–Ω–æ–∂–µ—Å—Ç–≤–∞ (set comprehension)
# –ë–µ—Ä—ë–º —Ç–æ–ª—å–∫–æ —á—ë—Ç–Ω—ã–µ —á–∏—Å–ª–∞ –∏–∑ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ 1‚Äì10 –∏ –≤–æ–∑–≤–æ–¥–∏–º –∏—Ö –≤ –∫–≤–∞–¥—Ä–∞—Ç
even_squares = {num ** 2 for num in range(1,11) if num % 2 == 0}

print(even_squares)



# # ===============================================
# # 2. –î–∞–Ω —Å–ø–∏—Å–æ–∫:
# # words = ["apple", "banana", "cherry", "apple", "banana", "date", "cherry"]
# # –°–æ–∑–¥–∞–π—Ç–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Å–ª–æ–≤, –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–≤ –∏—Ö –≤ –≤–µ—Ä—Ö–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤ (—Å –¥—É–±–ª–∏–∫–∞—Ç–∞–º–∏)
words = ["apple", "banana", "cherry", "apple", "banana", "date", "cherry"]

# –°–æ–∑–¥–∞—ë–º –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏–∑ —Å–ª–æ–≤, –ø—Ä–µ–¥–≤–∞—Ä–∏—Ç–µ–ª—å–Ω–æ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–≤ –∫–∞–∂–¥–æ–µ –≤ –≤–µ—Ä—Ö–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
unique_upper_words = {word.upper() for word in words}

print(unique_upper_words)



# # ===============================================
# # 3. –î–∞–Ω —Å–ª–æ–≤–∞—Ä—å:
# # grades = {"Alice": 85, "Bob": 78, "Charlie": 92, "David": 60, "Eve": 88}
# # –ò—Å–ø–æ–ª—å–∑—É—è –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä, —Å–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ:
# # –ï—Å–ª–∏ –æ—Ü–µ–Ω–∫–∞ –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–∞ 80, –∑–∞–ø–∏—Å–∞—Ç—å "–æ—Ç–ª–∏—á–Ω–æ".
# # –ï—Å–ª–∏ –º–µ–Ω—å—à–µ 80, –∑–∞–ø–∏—Å–∞—Ç—å "—É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ".
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å: –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞ ‚Üí —á–∏—Å–ª–æ–≤–∞—è –æ—Ü–µ–Ω–∫–∞
grades = {"Alice": 85, "Bob": 78, "Charlie": 92, "David": 60, "Eve": 88}

# –°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å —Å –ø–æ–º–æ—â—å—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ —Å–ª–æ–≤–∞—Ä—è (dict comprehension)
# –î–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–º–µ–Ω–∏ –∏ –æ—Ü–µ–Ω–∫–∏: –µ—Å–ª–∏ –æ—Ü–µ–Ω–∫–∞ >= 80 ‚Üí "–æ—Ç–ª–∏—á–Ω–æ", –∏–Ω–∞—á–µ ‚Üí "—É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ"
result = {name: "–æ—Ç–ª–∏—á–Ω–æ" if score >= 80 else "—É–¥–æ–ª–∏—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ" for name, score in grades.items()}

print(result)



# # ===============================================
# # 4. –î–∞–Ω–æ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —Å–ª–æ–≤:
# # text = {"Python", "automation", "programming", "testing"}
# # –°–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á–∏ ‚Äì —ç—Ç–æ —Å–ª–æ–≤–∞ –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤–∞, –∞ –∑–Ω–∞—á–µ–Ω–∏—è ‚Äì –¥–ª–∏–Ω–∞ –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞.
# # ===============================================

# –ò—Å—Ö–æ–¥–Ω–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ —Å–ª–æ–≤ (–±–µ–∑ –¥—É–±–ª–∏–∫–∞—Ç–æ–≤, –ø–æ—Ä—è–¥–æ–∫ –Ω–µ –≤–∞–∂–µ–Ω)
text = {"Python", "automation", "programming", "testing"}

# –°–æ–∑–¥–∞—ë–º —Å–ª–æ–≤–∞—Ä—å —Å –ø–æ–º–æ—â—å—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ —Å–ª–æ–≤–∞—Ä—è (dict comprehension)
# –ö–ª—é—á ‚Äî —Å–∞–º–æ —Å–ª–æ–≤–æ, –∑–Ω–∞—á–µ–Ω–∏–µ ‚Äî –µ–≥–æ –¥–ª–∏–Ω–∞ (len(word))
word_lenghts_= {word: len(word) for word in text}

print(word_lenghts_)



# # ===============================================
# # 5. –°–æ–∑–¥–∞–π—Ç–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á–∏ ‚Äì —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ n (–¥–ª—è –ø—Ä–∏–º–µ—Ä–∞ –ø—É—Å—Ç—å n = 10), –∞ –∑–Ω–∞—á–µ–Ω–∏—è ‚Äì –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ –∫–ª—é—á–∞.
# # –ü—Ä–∏–º–µ—Ä –¥–ª—è n = 3:
# #
# # {
# # 1: {1},
# # 2: {1, 4},
# # 3: {1, 4, 9}
# # }
# # ===============================================

# n = –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ, –≤–µ—Ä—Ö–Ω—è—è –≥—Ä–∞–Ω–∏—Ü–∞
n = 10
# –°–æ–∑–¥–∞—ë–º –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ª–æ–≤–∞—Ä—å —Å –ø–æ–º–æ—â—å—é –≥–µ–Ω–µ—Ä–∞—Ç–æ—Ä–∞ —Å–ª–æ–≤–∞—Ä—è (dict comprehension)
# –ö–ª—é—á ‚Äî —á–∏—Å–ª–æ i –æ—Ç 1 –¥–æ n
# –ó–Ω–∞—á–µ–Ω–∏–µ ‚Äî –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ i
nested_dict = {i: {j ** 2 for j in range(1, i + 1)} for i in range(1, n + 1)}

print(nested_dict)




