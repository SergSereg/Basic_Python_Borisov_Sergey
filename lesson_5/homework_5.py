letters_copy1 = letters_original.copy() # Способ 1: метод .copy() (рекомендуется — самый читаемый)

letters_copy2 = list(letters_original) # Способ 2: конструктор list()

print(f"\nЧерез .copy():   {letters_copy1}") # Через .copy():   ['a', 'b', 'c']
print(f"Через list():    {letters_copy2}") # Через list():    ['a', 'b', 'c']

## Способ 1: оператор `is` (сравнение идентичности)
is_different_1 = letters_original is not letters_copy1
is_different_2 = letters_original is not letters_copy2

print(f"\nletters_original in letters_copy1 → {is_different_1}")
print(f"letters_original in letters_copy2 → {is_different_2}")

## Способ 2: функция id() (уникальный адрес объекта в памяти)
id_orig = id(letters_original)
id_copy1 = id(letters_copy1)
id_copy2 = id(letters_copy2)

print(f"\nid(original) = {id_orig}")
print(f"id(copy1)    = {id_copy1}")
print(f"id(copy2)    = {id_copy2}")

